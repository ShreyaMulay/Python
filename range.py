# range(start , end ,displacement)

# start is default 0
# end is compulsory 
# displacement is default 1

# range function return a list

print("Range :", range(5))

print("List using range :", list(range(5)))

print("List using range :", list(range(3,8)))

print("List using range :", list(range(3,12,2)))

print("List using range :", list(range(20,0,-1)))


